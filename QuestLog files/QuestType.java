
/*There are many different types of archetypes of quests.
 * 
 * The binary quest refers to task that are either done or not ie. did you take out the trash
 * 
 * Progression refers to multiple/repetitive tasks ie do your homeworks (4/6)
 * 
 * Threshold refers to task that require a minimum amount to complete, but encourage to do more
 * ie. number of hours practicing music. This particular quest can have greater rewards 
 * depending on what threshold you manage to fulfill
 *  
 * Counter quests do not have a specific completion requirement but will simply count how many 
 * times you have done that specific action*/
public enum QuestType {
	BINARY, PROGRESSION, THRESHOLD, COUNTER;
}

